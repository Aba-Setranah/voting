package com.model;

/**
 *
 * @author Selinam
 */
public class ElectionVote {
    private String name;
    private int votes;

    public ElectionVote() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getVotes() {
        return votes;
    }

    public void setVotes(int votes) {
        this.votes = votes;
    }

    @Override
    public String toString() {
        return "ElectionVote{" + "name=" + name + ", votes=" + votes + '}';
    }
    
    
}
